static inline const uint8_t *font_get(char ch) {
    if (ch >= '0' && ch <= '9') return FONT_DATA[ch - '0'];
    if (ch >= 'a' && ch <= 'z') return FONT_DATA[10 + ch - 'a'];
    switch (ch) {
        case '%': return FONT_DATA[36];
        case '.': return FONT_DATA[37];
        case ':': return FONT_DATA[38];
        case '-': return FONT_DATA[39];
        case '/': return FONT_DATA[40];
        case ' ': return FONT_DATA[41];
        case '$': return FONT_DATA[42];
        case '_': return FONT_DATA[43];
        case ',': return FONT_DATA[44];
        default:  return NULL;
    }
}
